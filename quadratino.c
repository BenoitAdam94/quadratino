void playSFX(UINT8 id){ // Some noise
  if (id==1){
    NR21_REG = 0x7F;  
    NR22_REG = 0x7F;
    NR23_REG = (32-1)*16;
    NR24_REG = 0x86;
  }
  if (id==2){
    NR21_REG = 0x7F;
    NR22_REG = 0x7F;
    NR23_REG = (30)*2;
    NR24_REG = 0x86;
  }
}

void stopSFX(){
  NR21_REG = 0x00;    //Silence
  NR22_REG = 0x00;
  NR23_REG = 0x00;
  NR24_REG = 0x00;
}

void updateInputBuffer(){
  if(counter & J_UP) lastb = 1;       // buffer joypad input
  if(counter & J_DOWN) lastb = 2;
  if(counter & J_LEFT) lastb = 3;
  if(counter & J_RIGHT) lastb = 4;
  if(counter) justStarted=0;
}

void VBLwait(UINT8 qty){
  for(counter = 0; counter < qty; counter++){
    wait_vbl_done();
  }
}

